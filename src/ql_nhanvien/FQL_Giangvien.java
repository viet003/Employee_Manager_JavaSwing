    private Integer Check_Number(String value) {
        try {
            return Integer.parseInt(value);
        } catch (NumberFormatException e) {
            return null;
        }           
    }
    
    private boolean Check_TT() {
        if(_MaGV.getText().equals("") || _Hoten.getText().equals("") || _Email.getText().equals(ABORT) || _Sdt.getText().equals("") || _Diachi.getText().equals("") || _CN.getText().equals("") || _Luong.getText().equals("")) {
            JOptionPane.showConfirmDialog(this, "Vui lòng điền đầy đủ thông tin!","Thông báo",JOptionPane.OK_OPTION);
            return false;
        } 
        if(Check_Number(_Sdt.getText()) == null) {
            JOptionPane.showConfirmDialog(this, "Số điện thoại không bao gồm ký tự chữ cái!!","Thông báo",JOptionPane.OK_OPTION);
            return false;
        }
        if(Check_Number(_Luong.getText()) == null) {
            JOptionPane.showConfirmDialog(this, "Lương không bao gồm ký tự chữ cái!","Thông báo",JOptionPane.OK_OPTION);
            return false;
        }
        return true;
    }
    
