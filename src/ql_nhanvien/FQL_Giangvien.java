    private void Timkiem() {
        if(cbb_Timkiem.getSelectedIndex() == -1) {
            JOptionPane.showConfirmDialog(this, "Vui lựa chọn trường thông tin dùng để tìm kiếm","Thông báo",JOptionPane.OK_OPTION);
            return;
        }
        if (_Timkiem.getText().equals("")) {
            Load_table();
        } else {
             if(cbb_Timkiem.getSelectedIndex() == 0) {
                String sql = "SELECT * FROM GIANGVIEN WHERE MAGV = '" + _Timkiem.getText()+"'";
                ArrayList<QL_Giangvien> List_Gv = new ArrayList<>();
                DefaultTableModel model = (DefaultTableModel)jTable_Giangvien.getModel();
                model.setRowCount(0);
                try {
                Connection conn = QL_Controller.getJDBCConnection();
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                // đọc dữ liệu vào list
                while(rs.next()) {
                    String Magv = rs.getString("MAGV");
                    String Hoten = rs.getNString("HOTEN");
                    String Gioitinh = rs.getNString("GIOITINH");
                    String Ngaysinh = rs.getString("NGAYSINH");
                    String Email = rs.getString("EMAIL");
                    String Sdt = rs.getString("SDT");
                    String Diachi = rs.getNString("DIACHI");
                    String CN = rs.getNString("CHUYENNGANH");
                    String Hang = rs.getNString("HANG");
                    String Luong = rs.getString("LUONG");
                    QL_Giangvien gv = new QL_Giangvien(Magv,Hoten, Gioitinh, Ngaysinh,Email, Sdt,Diachi,CN,Hang,Luong);
                    List_Gv.add(gv);
                    }

                    for(QL_Giangvien _ac : List_Gv) {
                        model.addRow(new Object[] {
                           _ac.getMagv(),_ac.getHoten(),_ac.getGioitinh(),_ac.getNgaysinh(),_ac.getEmail(),_ac.getSdt(),_ac.getDiachi(),_ac.getCN(),_ac.getHang(),_ac.getLuong()
                        });
                    }
                    conn.close();
                } catch(SQLException ex) {
                    JOptionPane.showInputDialog(this,"Lỗi" + ex.getMessage());
            }
        }
       
        }    
    }
